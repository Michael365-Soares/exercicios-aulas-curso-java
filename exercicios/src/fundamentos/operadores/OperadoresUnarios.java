package fundamentos.operadores;

public class OperadoresUnarios {
    public static void main(String[]args) {
    	int a=5;
    	a++;
    	System.out.println("a=6 pós-incremento a++= "+a);
    	--a;
    	System.out.println("a=5 pré-decremento --a= "+a);
    	++a;
    	System.out.println("a=6 pré-incremento ++a= "+a);
    	--a;
    	System.out.println("a=5 pré-decremento --a= "+a);
    }
}
